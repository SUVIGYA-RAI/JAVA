public class OperatorPrecedence {
    public static void main(String[] args) {
        int a = 6*5-34/2;
        int b = 6/5-34*2;
        System.out.println(a);
        System.out.println(b);

        // PRECEDENCE & ASSOCIATIVITY
        // Operator precedance work as
        // PEMDAS-
        // P - Parenthesis
        // E - Exponential
        // M - Multiplication
        // D - Division
        // A - Addition
        // S - Subtraction
    }
}
